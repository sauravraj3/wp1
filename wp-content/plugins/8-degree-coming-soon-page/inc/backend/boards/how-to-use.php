<h2>How to use</h2>
<p>Basically there are four settings panel that consists of certain functionalities.</p>
<h3>General Settings</h3>
<p>This is the main settings panel where we have the options to enable or disable coming soon page with options to show/hide
   fields in our coming soon page. This tab has 6 different sections.</p>
<p><strong>- Show/Hide Section:</strong>If we check on show, the section wil appear in our coming soon page and vice-versa.</p>
<p><strong>- Subscriber: </strong> Here we can set the labels for our subscriber form in frontend. We can also cofirm valid subscribers by enabling 'confirm email address'. After 'confirm email address' is checked, the visistor will receive confimation email in which they have to confirm themselves that they are valid by clicking the link in email they receive.</p>    
<p><strong>- Enable Countdown:</strong> This option will allow user to set the countdown timer clock which will be displayed in coming soon page. The user will have to check on 'enable countdown' and set the date using datepicker for countdown timer.</p>
<h3>Design Settings</h3>
<p>Design Settings panel consist of options to save the overall design layout for coming soon page.</p>
<p><strong>- Color: </strong>We can set the colors using color picker for various sections as stated in this tab</p>
<p><strong>-Background: </strong>We also have option to choose background type i.e. image or color from this section for our page. The plugin consists of pre-available images to set backgroud in the page and also have option to upload our
   own image to set the background.</p>
<p><strong>-Layouts: </strong>We can choose the pre-set layout design for Countdown timer and Subscriber Forms available in plugin.</p>   

<h3>Visitor Subscriptions</h3>
<p>This panel section will help us display the list of visitor subscribers. We can also export the list of subscribers in CSV which can help us to work with data.</p>

<h3>Extra Settings</h3>
<p>This section can save various functions like disabling coming soon page for admin users based on their respective roles. 
   We can also add google analytics code, search engine meta name/content in tag and prevent search engine to index the page.   </p>


